print("========================================")

for originalName, newName in pairs(nameMap) do
    local item = SkinLibrary:FindFirstChild(originalName)
    if item then
        if originalName == newName then
            print(string.format("= UNCHANGED: '%s'", originalName))
            sameNameCount = sameNameCount + 1
        else
            item.Name = newName
            print(string.format("✓ RENAMED: '%s' → '%s'", originalName, newName))
            changedCount = changedCount + 1
        end
    else
        print(string.format("✗ NOT FOUND: '%s' (would rename to '%s')", originalName, newName))
        notFoundCount = notFoundCount + 1
    end
end

